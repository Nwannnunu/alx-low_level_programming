#include <stdio.h>
/**
 * add - function to add 2 integer
 * a - first integer
 * b - second integer
 * return: Return a + b
 */

int add(int a, int b)
{
	return (a + b);
}
/* sub - function to subtract two number*/

int sub(int a, int b)
{
	return (a - b);
}
/* mul - function to mulyiply two number*/
int mul(int a, int b)
{
	return (a * b);
}
/*div - Function to divide two number*/

int div(int a, int b)
{
	return (a / b);
}
/* mod - function to derive the modulo of two number*/

int mod(int a, int b)
{
	return (a % b);
}
